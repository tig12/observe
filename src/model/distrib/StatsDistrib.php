    Manipulate statistical informations of distributions read from a file stats.csv,
    generated with php run-observe.php <study> stats <split>
    
    Arrays manipulated by StatsDistrib have the fields of self::STATS_CSV_FIELDS
    This array is noted <StatsDistrib>
    
    StatsDistrib provides informations for distributions of type distrib1 and distrib2,
    in a directory containing stats.csv generated by php run-observe.php <study> stats <split>
    
    The <StatsDistrib> of the distributions (returned by self::loadStats()) are stored on an array with this structure:
        [
          'birth' => [
            'day' => <StatsDistrib>
            'year' => <StatsDistrib>
            'planets' => [ 'SO' => <StatsDistrib>, 'MO' =>   ...   ]
            'aspects' => [ 'SO-MO' => <StatsDistrib>, 'SO-ME' =>   ... ]
          'birth-death' => [
            'age' => <StatsDistrib>
            'interaspects' => [ 'SO-SO' => <StatsDistrib>, 'SO-MO' =>   ... ]
        ]
        
    
